// Neste desafio, vamos aprender sobre constantes! As constantes são como variáveis, mas com uma diferença importante: uma vez que você define o valor de uma constante, não pode mudá-lo.
// É como se fosse uma “caixinha” que guarda os dados inseridos pelo usuário em uma parte da memória, com o intuito de armazenar um valor fixo.

// Como exemplo, vamos criar uma constante para uma notificação do nosso jogo.

