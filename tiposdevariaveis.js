// Existem diferentes tipos de variáveis em JavaScript, cada uma com suas características e usos específicos. Aqui estão os principais tipos de variáveis:

// 1. String*: Representa uma sequência de caracteres, como texto. Exemplo: 
let nome = "João"

// 2. Number: Representa números, tanto inteiros quanto decimais. Exemplo:
let idade = 25

// 3. Boolean: Representa um valor verdadeiro (true) ou falso (false). Exemplo:
let joaoEhHomem = true
let joaoEhMulher = false