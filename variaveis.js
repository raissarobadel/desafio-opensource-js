// Neste desafio, vamos aprender sobre variáveis! As variáveis são como caixinhas que guardam uma informação que pode >variar<. 
// É como se fosse uma “caixinha” que guarda os dados inseridos pelo usuário em uma parte da memória, com o intuito de armazenar um valor variável.

// Agora, vamos exemplificar com o exemplo de escolher o nome de um personagem para um jogo!

let nickname = "nome do personagem"; // Aqui, estamos criando uma variável chamada nickname e atribuindo a ela o valor "nome do personagem". Para textos, usamos aspas duplas ou simples.

console.log("Bem vindo(a) " + nickname); // O console.log é uma função que imprime na tela o que está entre parênteses.
